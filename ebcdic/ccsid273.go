// ccsid273Codec is the real CCSID 273 codec, replacing the earlier
// CCSID 37 stand-in. Built on a hand-rolled forward table so we
// don't depend on golang.org/x/text shipping a CodePage273.
var ccsid273Codec = newTableCodec("CCSID 273", 273, ccsid273Forward)

func init() {
	// Override the package-level CCSID273 stand-in with the real
	// table-based codec. Lives in init() so test ordering doesn't
	// matter.
	CCSID273 = ccsid273Codec
}

