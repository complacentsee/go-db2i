package ebcdic

import (
	"fmt"
	"unicode/utf8"
)

// tableCodec is a Codec backed by a fixed 256-entry byte->rune
// forward table. Encoding uses a built-on-init reverse map. Use for
// any single-byte EBCDIC variant (CCSID 273, 277, 278, 280, ...) we
// need beyond what golang.org/x/text/encoding/charmap ships.
type tableCodec struct {
	name    string
	ccsid   uint32
	forward [256]rune
	reverse map[rune]byte
}

func (c *tableCodec) CCSID() uint32 { return c.ccsid }

func (c *tableCodec) Encode(s string) ([]byte, error) {
	out := make([]byte, 0, len(s))
	for _, r := range s {
		b, ok := c.reverse[r]
		if !ok {
			// Substitute character (EBCDIC 0x3F) for unrepresentable
			// runes; matches CCSID37 behaviour.
			out = append(out, 0x3F)
			continue
		}
		out = append(out, b)
	}
	return out, nil
}

func (c *tableCodec) Decode(b []byte) (string, error) {
	out := make([]byte, 0, len(b)*2)
	for _, by := range b {
		r := c.forward[by]
		var buf [4]byte
		n := utf8.EncodeRune(buf[:], r)
		out = append(out, buf[:n]...)
	}
	return string(out), nil
}

func newTableCodec(name string, ccsid uint32, forward [256]rune) *tableCodec {
	c := &tableCodec{
		name:    name,
		ccsid:   ccsid,
		forward: forward,
		reverse: make(map[rune]byte, 256),
	}
	// First-byte-wins for runes appearing more than once (mostly
	// 0x00 substitute and a few control chars) -- harmless because
	// the forward table is what determines the round-trip.
	for i, r := range forward {
		if _, ok := c.reverse[r]; !ok {
			c.reverse[r] = byte(i)
		}
	}
	return c
}

